#include<bits/stdc++.h>
using namespace std;

int main() {

	int x=5;
	int y=2;
	cout << x*y <<"\n";
	cout << x/y <<"\n";
	cout << (float) x/y <<"\n";  // explicit type conversion
	cout <<"\n";
	// the process of changing a value from one data type to another
	// is called type conversion

	int p=8;
	float q=5;
	cout << p/q <<"\n";  // implicit type convertion int to float
    cout <<"\n";




	return 0;
}
