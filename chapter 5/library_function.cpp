#include<bits/stdc++.h>
//#include<cmath> - library function header
using namespace std;

int main() {

	cout << sqrt(16) << "\n";  // square root

	cout << cbrt(125) << "\n";  // cube root

	cout << max(7,5) << "\n";  // maximum

	cout << min(7,5) << "\n";  // minimum

	cout << pow(5,3) << "\n";  // power

	return 0;
}
